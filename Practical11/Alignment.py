def hamming_distance(seq1, seq2):
    distance = 0
    for i in range(len(seq1)):
        if seq1[i] == seq2[i]:
            distance = distance + 1
    return distance


def BLOSUM_score(seq1, seq2):
    score = 0
    for i in range(len(seq1)):
        for j in range(len(acid_code)):
            if acid_code[j] == seq1[i]:
                x = j
                break
        for j in range(len(acid_code)):
            if acid_code[j] == seq2[i]:
                y = j
                break
        score = score + matrix[x][y]
    return score


